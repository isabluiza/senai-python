# Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada.

numero = float(input('Digite o número: '))

dobro = numero * 2
triplo = numero * 3
raiz_quadrada = numero**(1/2)

print(f'A dobro de {dobro}'
      f'\nO triplo {triplo}'
      f'\nO triplo {raiz_quadrada}')